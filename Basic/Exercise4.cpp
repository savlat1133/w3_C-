// Write a program in C++ to print the sum of two numbers using variables.
#include <iostream>

using namespace std;

int main() {
    int a = 29;
    int b = 30;

    cout << "Print the sum of two numbers: " << endl;
    cout << "-----------------------------" << endl;
    cout << "The sum of " << a << " and " << b << " is " << a + b << endl;

    return 0;
    
}
