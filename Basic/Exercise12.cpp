 // Add Two Numbers from Keyboard

#include <iostream>

using namespace std;

int main() {
    cout << "Sum of two numbers : " << endl;
    cout << "----------------------" << endl;

    int num1, num2;
    cout << "Input 1st number : ";
    cin >> num1;
    cout << "Input 2nd number : ";
    cin >> num2;
    cout << "Sum of two numbers : " << num1 + num2 << endl;

    return 0;
}
